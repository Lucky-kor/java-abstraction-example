package final_example.v2;

// 다른 예제: 상수를 사용하는 클래스
class Constants {
    public static final double PI = 3.14159; // final 상수: 변경 불가
    public static final double E = 2.71828;

    public static void main(String[] args) {
        System.out.println("PI: " + PI);
        System.out.println("E: " + E);
    }
}
